# A dictionary is a collection of key-value pairs,
# allowing you to store and retrieve data in a flexible way. Dictionaries are enclosed in curly braces.
# For example:

student = {
    "name": "Alice",
    "age": 20,
    "major": "Computer Science"
}

print(student)

student_name = student["name"]  # Result is "Alice"

print(student_name)
