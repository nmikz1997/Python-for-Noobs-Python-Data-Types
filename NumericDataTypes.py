# Integers (int): Whole numbers without a decimal point.
x = 10
y = -5

print(x,"and", y)

# Floating-point numbers (float): Numbers that have a decimal point. For example:
a = 3.14
b = -0.001

print(a,"and", b)

# You can perform arithmetic operations with these data types:
result = x + a  # Result is 13.14

print(result)
