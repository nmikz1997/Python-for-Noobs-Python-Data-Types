# A string (str) is a sequence of characters enclosed in single or double quotes. For example:
name = "Alice"
greeting = 'Hello, ' + name
print(greeting)

# Finding the length of a string:
length = len(name)  # Result is 5
print(length)

# Slicing a string:
substring = greeting[0:8]  # Result is "Hello, "
print(substring)